PHRASE_PINYIN_MAP = {
    "音乐": "yinyue",
}

PHRASE_INITIALS_MAP = {
    "音乐": "yy",
}

_SORTED_PHRASES = sorted(PHRASE_PINYIN_MAP, key=len, reverse=True)

# Build a set for fast lookup
_ALL = dict(PINYIN_MAP)

# Chinese unicode ranges
_CHINESE_START = 0x4E00
_CHINESE_END = 0x9FFF


def _is_chinese(char: str) -> bool:
    """Return True if char is a Chinese character."""
    cp = ord(char)
    return _CHINESE_START <= cp <= _CHINESE_END


def _get_pinyin(char: str) -> str:
    """Get pinyin for a single Chinese character.

    Returns the pinyin syllable if found, otherwise returns the character itself.
    For unknown characters, uses a simple letter mapping fallback.
    """
    # First try the lookup table
    result = _ALL.get(char)
    if result:
        return result

    # Fallback: return the first letter of the character code for unknown chars
    # This is a pypinyin-style fallback for characters not in our table
    if _is_chinese(char):
        # Use character code as fallback - not ideal but better than nothing
        return char

    # Non-Chinese characters pass through
    return char


def _match_phrase(text: str, start: int) -> str | None:
    for phrase in _SORTED_PHRASES:
        if text.startswith(phrase, start):
            return phrase
    return None


def to_pinyin(name: str) -> str:
    """
    Convert a Chinese string to pinyin (no tones, lowercase, no spaces).
    Unknown characters are kept as-is.
    """
    result = []
    i = 0
    while i < len(name):
        phrase = _match_phrase(name, i)
        if phrase:
            result.append(PHRASE_PINYIN_MAP[phrase])
            i += len(phrase)
            continue

        ch = name[i]
        if _is_chinese(ch):
            result.append(_get_pinyin(ch))
        else:
            result.append(ch.lower() if ch.isalpha() else ch)
        i += 1
    # Remove any spaces (some entries might have spaces between syllables)
    return "".join(result).lower().replace(" ", "")


def to_initials(name: str) -> str:
    """
    Get pinyin initials (first letter of each syllable) for a Chinese string.
    """
    parts = []
    i = 0
    while i < len(name):
        phrase = _match_phrase(name, i)
        if phrase:
            parts.append(PHRASE_INITIALS_MAP[phrase])
            i += len(phrase)
            continue

        ch = name[i]
        if _is_chinese(ch):
            p = _get_pinyin(ch)
            if p and p[0].isalpha():
                parts.append(p[0].lower())
        else:
            # For non-Chinese, use the first letter if it's alphabetic
            if ch.isalpha():
                parts.append(ch.lower())
        i += 1

    return "".join(parts)


def build_search_keys(name: str):
    """
    Return a dict with original name and derived pinyin keys.

    Args:
        name: Chinese app name string

    Returns:
        dict with keys:
            - 'original': original name
            - 'full_pinyin': full pinyin (no tones, lowercase, no spaces)
            - 'initials': pinyin initials (first letter of each syllable)
    """
    full = to_pinyin(name)
    initials = to_initials(name)
    return {
        "original": name,
        "full_pinyin": full,
        "initials": initials,
    }
